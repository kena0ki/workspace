// https://atcoder.jp/contests/abc222/tasks/abc222_a
fn solve(scan: &mut Scanner<impl BufRead>, out: &mut impl Write) {
    let s = scan.token::<usize>();
    let mut dp = vec![0usize;s+1];
    let md = 1000000007;
    let mut sum = 1;
    for i in 3..s+1 {
        sum+=dp[i-3];
        sum %= md;
        dp[i] = sum;
        logln!("{:?}",dp);
    }
    writeln!(out, "{}", dp[s]).ok();
}

